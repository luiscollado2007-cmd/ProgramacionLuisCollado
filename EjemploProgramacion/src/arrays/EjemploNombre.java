package arrays;

public class EjemploNombre {
    public static void main(String[] args) {

        String[] nombre = new String[3];
        nombre[0] = "Radu";
        nombre[1] = "Luis";
        nombre[2] = "Ibrahim";
        System.out.println("Nombre 0: " + nombre[0]);
        System.out.println("Nombre 1: " + nombre[1]);
        System.out.println("Nombre 2: " + nombre[2]);
    }
}
